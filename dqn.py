def compute_td_loss(model, target_model, batch_size, gamma, replay_buffer):
    state, action, reward, next_state, done = replay_buffer.sample(batch_size)

    state = Variable(torch.FloatTensor(np.float32(state)))
    next_state = Variable(torch.FloatTensor(np.float32(next_state)).squeeze(1), requires_grad=True)
    action = Variable(torch.LongTensor(action))
    reward = Variable(torch.FloatTensor(reward))
    done = Variable(torch.FloatTensor(done))

    # implement the loss function here

    q_value = model(state).detach().numpy()
    next_q = model(next_state).detach().numpy()
    target_q = target_model(next_state).detach().numpy()

    q_value = q_value.gather(1, action.unsqueeze(1)).squeeze(1)
    next_q = target_q.gather(1, torch.max(next_q, 1)[1].unsqueeze(1)).squeeze(1)

    if done[0]:
        # for terminal
        y = reward
    else:
        # for non - terminal
        y = reward + gamma * next_q

    loss = (autograd.Variable(y.data) - q_value).pow(2)
    return loss


class ReplayBuffer(object):
    def __init__(self, capacity):
        self.buffer = deque(maxlen=capacity)

    def push(self, state, action, reward, next_state, done):
        state = np.expand_dims(state, 0)
        next_state = np.expand_dims(next_state, 0)

        self.buffer.append((state, action, reward, next_state, done))

    def sample(self, batch_size):
        # TODO: Randomly sampling data with specific batch size from the buffer
        state, action, reward, next_state, done = zip(*random.sample(self.buffer, batch_size))

        state = np.concatenate(state)
        next_state = np.concatenate(next_state)

        return state, action, reward, next_state, done

    def __len__(self):
        return len(self.buffer)
